class Datatype {
    public static void main(String args[]){
        // Declaration
        int x;
        float y;
        char z;
        boolean a;

        // Initialization
        x = 45;
        y = 225.2343f; // by default, decimal value is double so, it order to get it into float, use f.
        z = 'd';
        a = true;

        // priting
        System.out.println("Int: " + x);
        System.out.println("Double: " + y);
        System.out.println("Character: " + z);
        System.out.println("Boolean: " + a);
    }    
}
